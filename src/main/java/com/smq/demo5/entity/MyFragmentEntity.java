public class MyFragmentEntity {

    String touxiang;
    String name;
    String profession;
    int serverTimes;
    long serverIncome;
    long balance;
    String tel;

    public String getTouxiang() {
        return touxiang;
    }

    public void setTouxiang(String touxiang) {
        this.touxiang = touxiang;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getProfession() {
        return profession;
    }

    public void setProfession(String profession) {
        this.profession = profession;
    }

    public int getServerTimes() {
        return serverTimes;
    }

    public void setServerTimes(int serverTimes) {
        this.serverTimes = serverTimes;
    }

    public long getServerIncome() {
        return serverIncome;
    }

    public void setServerIncome(long serverIncome) {
        this.serverIncome = serverIncome;
    }

    public long getBalance() {
        return balance;
    }

    public void setBalance(long balance) {
        this.balance = balance;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }
}
